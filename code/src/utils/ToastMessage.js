import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

// Note: toast.configure() is deprecated in v9, removed it

export function showToast(msg) {
  return toast(msg, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
}

